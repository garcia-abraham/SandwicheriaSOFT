    public void limpiarTabla(TableModel tableModel) {

        int cols = tableModel.getColumnCount();

        int fils = tableModel.getRowCount();

        for (int i = 0; i <= fils - 1; i++) {

            for (int j = 1; j <= cols - 1; j++) {
                tableModel.setValueAt("", i, j);
            }

        }
    }

