    private void tablaProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaProductoMouseClicked
        int seleccion = tablaProducto.rowAtPoint(evt.getPoint());
      //String numCadena = this.getCodigoProducto().getText();
      //int numEntero = Integer.parseInt(numCadena);//transforma el string a  numero

        boolean bandera = false;

            Producto producto = this.getTerminal().ingresarCodigoProducto(seleccion+1);
            if (producto != null) {
                this.setProducto(producto);
                bandera = true;

                for (Agregado agregado : producto.getAgregados()) {
                    Object[] fila = new Object[5];
                    fila[0] = agregado.getCodigo();
                    fila[1] = agregado.getNombre();
                    fila[2] = agregado.getPrecio();
                    fila[3] = agregado.isIncluido();
                    fila[4] = agregado.isHabilitado();

                    this.getModeloTablaAgregado().addRow(fila);
                }

            }
            this.getTablaAgregado().setModel(this.getModeloTablaAgregado());

        
        if (!bandera) {
            ErrorCodigoProducto error = new ErrorCodigoProducto(this, true);
        }
    }//GEN-LAST:event_tablaProductoMouseClicked

