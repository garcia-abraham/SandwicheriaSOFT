    private void finalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizarActionPerformed
        // TODO add your handling code here:
        this.getTerminal().finalizarVenta();

        Comprobante comprobante = new Comprobante(this.getTerminal());

        comprobante.getModeloDescripcio().getColumnCount();
        comprobante.getModeloDescripcio().addColumn("Cantidad");
        comprobante.getModeloDescripcio().addColumn("Descripci√≥n");
        comprobante.getModeloDescripcio().addColumn("Precio Unitario");
        comprobante.getModeloDescripcio().addColumn("Total");

        for (LineaVenta lv : this.getTerminal().getVenta().getLineasVentas()) {
            Object[] fila = new Object[4];
            fila[0] = lv.getCantidad();
            fila[1] = lv.getProducto().getNombre();
            fila[2] = lv.getProducto().calcularPrecio();
            fila[3] = lv.getSubTotal();
            
            comprobante.getModeloDescripcio().addRow(fila);

        }
        comprobante.getImporteNeto().setText(String.valueOf(this.getTerminal().getVenta().getNeto()));
        comprobante.getImporteTotal().setText(String.valueOf(this.getTerminal().getVenta().getTotal()));

        comprobante.getDetalleComprobante().setModel(comprobante.getModeloDescripcio());

        
        this.dispose();

    }//GEN-LAST:event_finalizarActionPerformed

